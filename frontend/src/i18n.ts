const storedLocale = typeof localStorage !== "undefined" ? localStorage.getItem("trace-locale") : null;
const locale = ref<Locale>(storedLocale === "en" ? "en" : "zh");

function format(template: string, params?: Record<string, string | number>): string {
  if (!params) {
    return template;
  }
  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(params[key] ?? `{${key}}`));
}

export function setLocale(nextLocale: Locale) {
  locale.value = nextLocale;
  localStorage.setItem("trace-locale", nextLocale);
}

export function useI18n() {
  const t = (key: string, params?: Record<string, string | number>) => {
    const dict = dictionaries[locale.value];
    return format(dict[key] ?? dictionaries.en[key] ?? key, params);
  };

  return {
    locale,
    locales: ["zh", "en"] as Locale[],
    setLocale,
    t,
    isZh: computed(() => locale.value === "zh")
  };
}
